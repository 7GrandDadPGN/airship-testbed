export interface MobileButtonConfig {
	/**
	 * A path to an image.
	 */
	icon?: string;
	/**
	 * The icon's scale.
	 */
	scale?: Vector2;
	/**
	 * The button's minimum anchor.
	 */
	anchorMin?: Vector2;
	/**
	 * The button's maximum anchor.
	 */
	anchorMax?: Vector2;
	/**
	 * The button's pivot.
	 */
	pivot?: Vector2;
}
